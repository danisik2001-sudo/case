    mounted() {
        this.setStat();
        this.getNotifications();
    },
    methods: {
        formatDate(dateString) {
            const date = new Date(dateString);
            const day = String(date.getDate()).padStart(2, "0");
            const month = String(date.getMonth() + 1).padStart(2, "0");
            const year = date.getFullYear(); // Год

            return `${day}.${month}.${year}`; // Формат даты без времени
        },

        async setStat() {
            await this.axios.get("/live/statistic").then((res) => {
                this.stat = res.data;
            });
        },
        async getNotifications() {
            await this.axios.get("/notification/get").then((res) => {
                this.notifications = res.data; // Используем правильное свойство
            });
        },
        logOut() {
            this.axios.post("/user/logout").then((res) => {
                this.$cookie.delete("token");
                window.location.reload();
            });
        },
        toggleDropLanguage() {
            this.dropLanguage = !this.dropLanguage;
        },
        toggleDropSettings() {
            this.dropSettings = !this.dropSettings;
        },
        setLang(lang) {
            this.$root.lang = lang;
            this.$cookie.set("lang", lang);
            this.$i18n.locale = lang;
        },
        openPayment() {
            this.$root.$emit("openPaymentModal");
        },
        openLoginModal() {
            this.$root.$emit("openLoginModal");
        },
    },
    sockets: {
        online(online) {
            this.online = online;
        },

        stats: function (data) {
            this.stat = { ...this.stat, ...data.live };
        },
    },
};
